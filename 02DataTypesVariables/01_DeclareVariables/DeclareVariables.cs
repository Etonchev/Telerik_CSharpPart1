using System;

class DeclareVariables
{
    static void Main()
    {
        ushort first = 52130;
        sbyte second = -115;
        uint third = 4825932;
        byte fourth = 97;
        short fifth = -10000;
        Console.WriteLine("{0}, {1}, {2}, {3}, {4}", first, second, third, fourth, fifth);
    }
}
